import type { Operation } from "@hermes-studio/protocol";
import type { RuntimeMessage } from "./i18n";

export type ProfileStatus = "working" | "waiting" | "idle" | "blocked";

export type Profile = {
  id: string;
  name: string;
  /** Optional local display alias kept by the Studio client. */
  displayName?: string | undefined;
  /** @deprecated Prefer displayName. Kept for demo data migration. */
  nameJa?: string | undefined;
  role: string;
  status: ProfileStatus;
  color: string;
  sessions: number;
  taskCount: number;
  memoryBytes: number;
  memoryNote: string;
  skills: string[];
  inheritedSkills: string[];
};

export type ChatMessage = {
  id: string;
  from: "user" | "agent" | "tool";
  kind?: "steer" | undefined;
  body: string;
  /** Locale-neutral copy generated by Studio itself. Hermes-provided text stays in body. */
  presentation?: { kind: "tool-fallback"; name?: string | undefined; phase: "running" | "complete" } | undefined;
  at: string;
  status?: "streaming" | "complete" | "failed" | "cancelled";
  /** Legacy in-memory shape migrated into ChatSession.operationEvidence before transcript replacement. */
  promptOperation?: {
    id: string;
    state: "pending" | "accepted" | "rejected" | "unconfirmed";
    message?: string | undefined;
  } | undefined;
};

export type ChatOperationEvidence = {
  id: string;
  kind: "prompt" | "steer";
  body: string;
  at: string;
  state: "pending" | "accepted" | "rejected" | "unconfirmed";
  message?: string | undefined;
};

export type ChatConnectionState = "disconnected" | "connecting" | "ready" | "error";
export type ChatHistoryState = "unloaded" | "loading" | "loaded" | "error";

export type ApprovalChoice = "once" | "session" | "always" | "deny";

export type ChatPendingInteraction =
  | {
      id: string;
      kind: "clarify";
      requestId: string;
      question: string;
      choices: string[];
      submitting: boolean;
      error?: RuntimeMessage | undefined;
    }
  | {
      id: string;
      kind: "approval";
      approvalId: string;
      command?: string | undefined;
      description?: string | undefined;
      choices: ApprovalChoice[];
      allowPermanent: boolean;
      submitting: boolean;
      error?: RuntimeMessage | undefined;
    };

export type ChatSession = {
  /** Stable UI identity. It can exist before Hermes persists a session. */
  id: string;
  /** Durable Hermes state.db identity used by REST history and resume. */
  storedSessionId?: string | undefined;
  /** Process-local gateway identity, valid only for the current WebSocket. */
  liveSessionId?: string | undefined;
  profileId: string;
  title: string;
  /** Only local draft titles are translated at render time. Hermes titles remain verbatim. */
  titlePresentation?: "new-chat" | undefined;
  createdAt?: string | undefined;
  updatedAt?: string | undefined;
  lastMessagePreview?: string | undefined;
  status: "streaming" | "ready" | "waiting";
  messages: ChatMessage[];
  /** Bounded local RPC evidence. It is deliberately separate from Hermes' durable transcript. */
  operationEvidence?: ChatOperationEvidence[] | undefined;
  connectionState?: ChatConnectionState;
  historyState?: ChatHistoryState;
  historyPartial?: boolean;
  historyNotice?: RuntimeMessage | undefined;
  errorMessage?: RuntimeMessage | undefined;
  remoteKind?: "demo" | "stored" | "draft" | undefined;
  streamingMessageId?: string | undefined;
  pendingInteraction?: ChatPendingInteraction | undefined;
  steerPending?: boolean | undefined;
  steerOperationId?: string | undefined;
  interruptPending?: boolean | undefined;
  interruptOperationId?: string | undefined;
  readOnly?: boolean;
  /** Preferred model for session.create / display. */
  model?: string | undefined;
  /** Preferred provider for session.create / display. */
  provider?: string | undefined;
  /** Preferred reasoning_effort for new Hermes sessions (omit when empty/default). */
  reasoningEffort?: string | undefined;
  /** Local follow-up chips after the latest assistant reply. */
  followUpSuggestions?: string[] | undefined;
  /**
   * Studio client link: this chat was opened from a Kanban card ("Ask assignee").
   * Not a Hermes durable identity; used for reuse and chrome only.
   */
  sourceCardId?: string | undefined;
  sourceCardTitle?: string | undefined;
  /** One-shot guard so card context is attached only to the first user-authored prompt. */
  sourceCardSeeded?: boolean | undefined;
  /** Card context for the first user-authored prompt; never auto-sent. */
  pendingCardSeed?: string | undefined;
};

export type TaskStatus = "triage" | "todo" | "scheduled" | "ready" | "running" | "blocked" | "review" | "done" | "archived";
export type TaskWritableStatus = "triage" | "todo" | "scheduled" | "ready" | "blocked" | "done" | "archived";

